def get_grade(score) 

	if(score >=90)
	 return "A"
	end
	if(score>=80 && score <90)
		return "B"
	end
	if(score >= 70 && score < 80)
		return "C"
	end
	if(score >= 60 && score < 70)
		return "D"
	end
	return "F"
end




	